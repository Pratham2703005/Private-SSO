// ============================================================================
// Widget Initialization
// ============================================================================

export function initializeWidget() {
  // Check third-party cookies on init
  if (detectThirdPartyCookieBlock()) {
    console.warn("[Widget] Third-party cookies blocked on init");
    state.thirdPartyCookiesBlocked = true;
    showFallbackUI();
    return;
  }

  // Set up message listener
  receiveMessage((msg) => {
    switch (msg.type) {
      case "REQUEST_ACCOUNTS":
        handleRequestAccounts(msg);
        break;
      case "SWITCH_ACCOUNT":
        handleSwitchAccount(msg);
        break;
      case "LOGOUT_APP":
        handleLogoutApp(msg);
        break;
      case "LOGOUT_GLOBAL":
        handleLogoutGlobal(msg);
        break;
      default:
        console.warn("[Widget] Unknown message type:", msg.type);
    }
  });

  // Notify parent that widget is ready
  const nonce = generateNonce();
  const requestId = generateRequestId();

  sendMessageToParent({
    type: "WIDGET_READY",
    nonce,
    requestId,
    payload: {
      idpUrl: IDP_URL,
    },
  });
}

